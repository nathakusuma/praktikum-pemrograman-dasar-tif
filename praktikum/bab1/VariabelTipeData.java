public class VariabelTipeData {
    public static void main(String[] args) {
        // [tipeData] [namaVariabel]
        // reference => String
        // primitif => int, float, double, etc

        // deklarasi variabel
        byte angkaKecil;
        long angkaBesar;

        double bilanganKoma;
        float pi;

        // inisialisasi variabel
        // operator assignment
        // namaVariabel = nilai;

        angkaKecil = 10;
        angkaBesar = 1000000;

        bilanganKoma = 1.2345678;
        pi = 3.14F;

        // deklarasi & inisialisasi
        String kalimat = "Hello World";
        int angkaSedang = 500000;

        // print
        System.out.println(angkaKecil);
        System.out.println(angkaSedang);
        System.out.println(angkaBesar);
        System.out.println(bilanganKoma);
        System.out.println(pi);
        System.out.println(kalimat);
    }
}
